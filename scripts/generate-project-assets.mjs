import { readdir, writeFile } from 'node:fs/promises';
import { extname, join, relative, resolve, sep } from 'node:path';

const root = resolve('public/projects');
const output = resolve('src/data/project-assets.ts');
const supported = new Set(['.avif', '.gif', '.jpeg', '.jpg', '.mov', '.mp4', '.png', '.svg', '.webm', '.webp']);

async function collect(directory) {
	const entries = await readdir(directory, { withFileTypes: true });
	const assets = [];
	for (const entry of entries) {
		const path = join(directory, entry.name);
		if (entry.isDirectory()) assets.push(...await collect(path));
		else if (supported.has(extname(entry.name).toLowerCase())) assets.push('/projects/' + relative(root, path).split(sep).join('/'));
	}
	return assets;
}

const assets = (await collect(root)).sort((left, right) => left.localeCompare(right, 'en'));
await writeFile(output, `// Generated by scripts/generate-project-assets.mjs. Do not edit manually.\nexport const projectAssets = ${JSON.stringify(assets, null, '\t')} as const;\n`, 'utf8');
console.log(`Generated ${assets.length} project asset paths.`);
